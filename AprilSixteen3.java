public class AprilSixteen3{
	public static void main(String[] args) {
		
		System.out.println("东京\t大阪\t名古屋");
		System.out.println("东京\n大阪\n三重");
		System.out.println("东京\\大阪\\三重");
		System.out.println("老师说：东京\"大阪\"三重");
		System.out.println("老师说：\'东京大阪三重\'");
		System.out.println("名侦探柯南\r东京");
		
	}
}
//  \t   一个制表位，实现对齐功能
//  \n   换行
//  \\   文字内1个斜杠  斜杠增加以此类推
//  \"   文字内标重点 ''
//  \'   文字内标重点 '
//  \r   回车=换行