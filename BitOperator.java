public class BitOperator{
	public static void main(String[] args) {
		
		//推导过程
		//1. 先得到 2的补码---》2的原码 00000000 00000000 00000000 00000010
		//   2的补码----》00000000 00000000 00000000 00000010
		//2. 3的原码----》00000000 00000000 00000000 00000011
		//	 3的补码----》00000000 00000000 00000000 00000011
		//3. 按位&
		//00000000 00000000 00000000 00000010
		//00000000 00000000 00000000 00000011
		//结果
		//00000000 00000000 00000000 00000010 此运算后的补码与原码相同
		//转为10进制  2
		System.out.println(2&3);
		
		//推导
		//先得到-2的原码-----》10000000 00000000 00000000 00000010
		//-2的 反码---------》11111111 11111111 11111111 11111101
		//-2的 补码---------》11111111 11111111 11111111 11111110
		//～-2 操作---------》00000000 00000000 00000000 00000001
		//运算后的原码-------》00000000 00000000 00000000 00000001==》1
		System.out.println(~-2);
		//推导
		//得到2的补码-------》00000000 00000000 00000000 00000010
		//~2操作-----------》11111111 11111111 11111111 11111101 运算后的补码
		//运算后的反码------》11111111 11111111 11111111 11111100
		//运算后的原码------》10000000 00000000 00000000 00000011--》-3
		System.out.println(~2);
	}
}